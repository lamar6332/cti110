#Obtain number from the user.
num = int(input("Enter your number here: "))

#Decisions based on user input.
if num == 1:
    print ("I")
elif num == 2:
    print ("II")
elif num == 3:
    print ("III")
elif num == 4:
    print ("IV")
elif num == 5:
    print ("V")
elif num == 6:
    print ("VI")
elif num == 7:
    print ("VII")
elif num == 8:
    print ("VIII")
elif num == 9:
    print ("IX")
elif num == 10:
    print ("X")
else:
    print ("Pick a number between one and ten")
